"""
synthesizer.py — Final report generation.

Workspace-first. Accepts a `Workspace` (preferred) or a legacy `SchemaInfo`
plus condensed `AnalysisMemory`, and produces markdown or HTML reports
optionally enriched with web research findings and mutation summaries.
"""

from __future__ import annotations
import time
from pathlib import Path
from typing import TYPE_CHECKING

from llm    import call_chat
from memory import AnalysisMemory
from utils  import get_logger

if TYPE_CHECKING:
    from core   import Workspace
    from schema import SchemaInfo

log = get_logger("synthesizer")

_REPORTS_DIR = Path("output") / "reports"


# ---------------------------------------------------------------------------
# LLM prompts
# ---------------------------------------------------------------------------

_SYSTEM = """\
You are a professional data analyst writing a structured report.
Write clear, concise markdown. Reference specific numbers from the analysis.
Do not invent data that is not present in the provided results.\
"""

_USER_TEMPLATE = """\
{workspace_block}

ORIGINAL REQUEST
"{query}"

ANALYSIS RESULTS  ({n_steps} steps, {n_ok} succeeded)
{memory_context}

{web_section}{mutation_section}CHARTS GENERATED
{chart_list}

Write a professional markdown report:
# [Descriptive Title]

## Executive Summary
(3–5 sentences — cite specific numbers and named workspace objects)

## Findings
(One ## section per successful analysis step — use the step description as heading)

## Key Insights
(3–6 bullets — specific numbers only, no generalities)

## Recommendations
(2–4 actionable bullets)

{web_instructions}{mutation_instructions}Rules:
- Reference only numbers present in the results above.
- Refer to objects by their workspace names (spreadsheets, documents, tables).
- Skip failed steps silently.
- Keep under 900 words.
- No markdown fences around the output.\
"""

_WEB_SECTION_TEMPLATE   = "WEB RESEARCH CONTEXT\n{web_context}\n\n"
_WEB_INSTRUCTIONS       = "- Include a \"## External Context\" section summarising the web findings.\n"
_MUTATION_SECTION_TEMPLATE = "MUTATIONS APPLIED\n{mutation_context}\n\n"
_MUTATION_INSTRUCTIONS  = "- Include a \"## Data Updates\" section listing the mutations made.\n"


